CREATE PROCEDURE PRIMENO
@NO INT
AS
DECLARE @I INT , @FLAG INT
SET @I=2
SET @FLAG=1
	WHILE @I<=@NO/2
BEGIN
	IF(@N0%@I=0)
BEGIN
	SET @FLAG=0
	BREAK
END
	SET @I=@I+1
END
	IF(@FLAG=1)
	PRINT 'PRIME'
	ELSE
	PRINT 'NO PRIME'
EXEC PRIMENO 13
	  


